// ── Context ───────────────────────────────────────────────────────────────────

type TFn = (key: keyof typeof en, vars?: Record<string, string | number>) => string

interface I18nCtx { lang: Lang; setLang: (l: Lang) => void; t: TFn }

const Ctx = createContext<I18nCtx>({
  lang: 'ar',
  setLang: () => {},
  t: (k) => k as string,
})

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const stored = (typeof localStorage !== 'undefined' && localStorage.getItem('lang')) as Lang | null
  const [lang, setLangState] = useState<Lang>(stored ?? 'ar')

  const setLang = useCallback((l: Lang) => {
    localStorage.setItem('lang', l)
    setLangState(l)
  }, [])

  useEffect(() => {
    document.documentElement.lang = lang
    document.documentElement.dir  = lang === 'ar' ? 'rtl' : 'ltr'
  }, [lang])

  const dict = lang === 'ar' ? ar : en

  const t: TFn = useCallback((key, vars) => {
    let str: string = dict[key] ?? (en[key] as string) ?? key as string
    if (vars) {
      Object.entries(vars).forEach(([k, v]) => {
        str = str.replaceAll(`{{${k}}}`, String(v))
      })
    }
    return str
  }, [dict])

  return <Ctx.Provider value={{ lang, setLang, t }}>{children}</Ctx.Provider>
}

export const useT = () => useContext(Ctx)
